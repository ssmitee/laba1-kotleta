//12.71 двумерные массивы
//Дан двумерный массив целых чисел. Определить:
//а) сумму элементов массива, больших 30;
//б) количество нечетных элементов массива;
//в) среднее арифметическое четных элементов массива;
//г) сумму тех элементов массива, сумма индексов которых равна s.

fun main() {
    // Пример двумерного массива
    val array = arrayOf(
        arrayOf(10, 32, 45),
        arrayOf(28, 54, 15),
        arrayOf(42, 31, 10)
    )

    // a) Сумма элементов массива, больших 30
    val sumGreaterThan30 = array.flatten().filter { it > 30 }.sum()
    println("Сумма элементов массива, больших 30: $sumGreaterThan30")

    // b) Количество нечетных элементов массива
    val oddCount = array.flatten().count { it % 2 != 0 }
    println("Количество нечетных элементов массива: $oddCount")

    // c) Среднее арифметическое четных элементов массива
    val evenElements = array.flatten().filter { it % 2 == 0 }
    val averageEven = if (evenElements.isNotEmpty()) evenElements.average() else 0.0
    println("Среднее арифметическое четных элементов массива: $averageEven")

    // d) Сумма элементов массива, сумма индексов которых равна s
    val s = 2 // пример значения s
    val sumWithIndexSumS = array.indices.sumBy { i ->
        array[i].indices.sumBy { j ->
            if (i + j == s) array[i][j] else 0
        }
    }
    println("Сумма элементов массива, сумма индексов которых равна $s: $sumWithIndexSumS")
}